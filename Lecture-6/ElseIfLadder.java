class ElseIfLadder
{
    public static void main(String[] args)
    {
        int val = 20;

        if(val == 1)
        {
            System.out.println("One");
        }
        else if(val == 2)
        {
            System.out.println("Two");
        }
        else if(val == 3)
        {
            System.out.println("Three");
        }
        else
        {
            System.out.println("Not Match");
        }
    }
}