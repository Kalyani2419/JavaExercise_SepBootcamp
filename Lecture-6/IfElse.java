class IfElse
{
    public static void main(String[] args)
    {
        int val1=15;
        int val2=6;

        if(val1>val2)
        {
            System.out.println(val1+" is greater than "+val2);
        }
        else
        {
            System.out.println(val2+" is greater than "+val1);
        }
    }
}